def parent_base_url(parent: str) -> str | None:
    text = read_text(ROOT / f"{parent}-cli" / CLIENT_GO)
    m = re.search(r'const BaseURL = "([^"]+)"', text)
    return m.group(1) if m else None


def patch_child_base_url(cli_dir: Path, parent: str) -> None:
    url = parent_base_url(parent)
    if not url:
        return
    path = cli_dir / CLIENT_GO
    text = read_text(path)
    text = re.sub(r'const BaseURL = "[^"]+"', f'const BaseURL = "{url}"', text, count=1)
    path.write_text(text, encoding="utf-8")


def copy_parent_client_files(cli_dir: Path, parent: str, cat: str) -> None:
    names = HOTEL_CLIENT_FILES if cat == "hotel" else AIRLINE_CLIENT_FILES
    parent_dir = ROOT / f"{parent}-cli/internal/client"
    child_dir = cli_dir / "internal/client"
    for name in names:
        src = parent_dir / name
        if not src.is_file():
            continue
        body = read_text(src)
        if body.startswith("// " + WIRED_MARK):
            body = body.split("\n", 1)[1]
        header = f"// {WIRED_MARK} — copied from {parent}-cli/{name}\n"
        (child_dir / name).write_text(header + body, encoding="utf-8")
    patch_child_base_url(cli_dir, parent)


def remove_parent_go_mod_refs(go_mod_path: Path, parent: str) -> None:
    if not go_mod_path.is_file():
        return
    text = read_text(go_mod_path)
    module = f"github.com/fbelchi/{parent}-cli"
    lines = [
        ln
        for ln in text.splitlines()
        if module not in ln or "replace" in ln and "travelkit" in ln
    ]
    # also drop replace for parent
    lines = [ln for ln in lines if f"replace {module}" not in ln]
    go_mod_path.write_text("\n".join(lines).rstrip() + "\n", encoding="utf-8")


def should_wire(search_path: Path, force: bool) -> bool:
    if is_scaffold_stub(search_path):
        return True
    return force and is_wired(search_path)


def wire_slug(
    slug: str,
    parent: str,
    cat: str,
    brand_name: str,
    dry_run: bool,
    force: bool,
    strategy: str,
) -> bool:
    cli_dir = ROOT / f"{slug}-cli"
    search_path = cli_dir / SEARCH_GO
    if not should_wire(search_path, force):
        return False
    parent_search = ROOT / f"{parent}-cli" / SEARCH_GO
    if not is_implemented(parent_search):
        return False
    if slug == parent:
        return False

    if dry_run:
        print(f"would wire {slug} -> {parent} ({cat}, {strategy})")
        return True

    if strategy == "copy-client":
        copy_parent_client_files(cli_dir, parent, cat)
        remove_parent_go_mod_refs(cli_dir / GO_MOD, parent)
    else:
        content = (
            hotel_exec_delegate(parent, brand_name)
            if cat == "hotel"
            else airline_exec_delegate(parent, brand_name)
        )
        search_path.write_text(content, encoding="utf-8")
        remove_parent_go_mod_refs(cli_dir / GO_MOD, parent)

    print(f"wired {slug} -> {parent} ({strategy})")
    return True


def count_stubs() -> int:
    return sum(1 for d in ROOT.glob("*-cli") if is_scaffold_stub(d / SEARCH_GO))


def verify_build(slugs: list[str]) -> tuple[int, int]:
    ok = fail = 0
    for slug in slugs:
        cli_dir = ROOT / f"{slug}-cli"
        r = subprocess.run(
            ["go", "build", "-o", slug, f"./cmd/{slug}"],
            cwd=cli_dir,
            capture_output=True,
            text=True,
        )
        if r.returncode == 0:
            ok += 1
            (cli_dir / slug).unlink(missing_ok=True)
        else:
            fail += 1
            print(f"BUILD FAIL {slug}: {r.stderr.strip()[:300]}", file=sys.stderr)
    return ok, fail


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--dry-run", action="store_true", help="print actions only")
    ap.add_argument("--verify", action="store_true", help="go build wired slugs")
    ap.add_argument("--sync-groups", action="store_true", help="refresh groups.json parent maps")
    ap.add_argument("--force", action="store_true", help="re-wire files already marked wired")
    ap.add_argument(
        "--strategy",
        choices=("exec", "copy-client"),
        default="exec",
        help="exec parent CLI (default) or copy parent client/*.go sources",
    )
    args = ap.parse_args()

    doc = enrich_groups(load_groups_doc())
    if args.sync_groups or (not args.dry_run and not args.force):
        save_groups_doc(doc)

    stubs_before = count_stubs()
    wired: list[str] = []

    for g in groups_list(doc):
        slug = g["slug"]
        parent = parent_for_slug(slug, doc)
        if wire_slug(slug, parent, g["cat"], default_brand(g), args.dry_run, args.force, args.strategy):
            wired.append(slug)

    stubs_after = count_stubs() if not args.dry_run else stubs_before - len(wired)

    brands_delegated = sorted(
        {
            b
            for g in groups_list(doc)
            for b in g["brands"]
            if parent_for_slug(g["slug"], doc) != g["slug"]
            and g["slug"] in wired
        }
    )

    print(f"stubs_before={stubs_before}")
    print(f"stubs_after={stubs_after}")
    print(f"wired_count={len(wired)}")
    if wired:
        print("wired_slugs=" + ",".join(wired))
    print(f"brands_delegated={len(brands_delegated)}")

    if args.verify and wired and not args.dry_run:
        for slug in wired:
            cli_dir = ROOT / f"{slug}-cli"
            subprocess.run(["go", "mod", "tidy"], cwd=cli_dir, check=False)
        ok, fail = verify_build(wired)
        print(f"build_ok={ok} build_fail={fail}")
        return 1 if fail else 0

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
