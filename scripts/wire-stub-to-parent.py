#!/usr/bin/env python3
"""Wire stub CLIs to a parent slug's implemented search API.

For each *-cli whose internal/client/search.go is still a scaffold stub:
  - if groups.json maps slug -> parent P and P != slug
  - and parent P has a non-stub search.go
  - replace search.go with a delegate (default: exec parent CLI binary with --brand)

Go modules cannot import another CLI's internal/client package, so the default
strategy shells out to the parent binary. Use --strategy copy-client to copy
parent client/*.go sources into the child (same package, parent's BaseURL).

Re-run after parallel agents land parent APIs. Idempotent (skips already-wired files).
"""

from __future__ import annotations

import argparse
import re
import shutil
import subprocess
import sys
from pathlib import Path

from groups_lib import (
    ROOT,
    default_brand,
    enrich_groups,
    groups_list,
    load_groups_doc,
    parent_for_slug,
    save_groups_doc,
)

STUB_MARK = "search not yet implemented"
WIRED_MARK = "Code generated by scripts/wire-stub-to-parent.py"
SEARCH_GO = Path("internal/client/search.go")
CLIENT_GO = Path("internal/client/client.go")
GO_MOD = Path("go.mod")

HOTEL_CLIENT_FILES = ("search.go", "read.go", "availability.go")
AIRLINE_CLIENT_FILES = ("search.go", "read.go")


def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8")


def is_scaffold_stub(search_path: Path) -> bool:
    if not search_path.is_file():
        return False
    text = read_text(search_path)
    return STUB_MARK in text and WIRED_MARK not in text


def is_wired(search_path: Path) -> bool:
    return search_path.is_file() and WIRED_MARK in read_text(search_path)


def is_implemented(search_path: Path) -> bool:
    if not search_path.is_file():
        return False
    text = read_text(search_path)
    return STUB_MARK not in text and "func (c *Client) Search(" in text


def json_str(s: str) -> str:
    return '"' + s.replace("\\", "\\\\").replace('"', '\\"') + '"'


def hotel_exec_delegate(parent: str, default_brand_name: str) -> str:
    env_key = parent.upper().replace("-", "_")
    return f"""package client

// {WIRED_MARK} — exec delegate to {parent} CLI (--brand).

import (
\t"encoding/json"
\t"fmt"
\t"os"
\t"os/exec"
\t"path/filepath"
\t"strings"
)

const wireParentSlug = {json_str(parent)}

func wireParentBin() (string, error) {{
\tenvKey := strings.ToUpper(strings.ReplaceAll(wireParentSlug, "-", "_")) + "_BIN"
\tif p := os.Getenv(envKey); p != "" {{
\t\treturn p, nil
\t}}
\tfor _, rel := range []string{{
\t\tfilepath.Join("..", wireParentSlug+"-cli", wireParentSlug),
\t\twireParentSlug,
\t}} {{
\t\tif st, err := os.Stat(rel); err == nil && !st.IsDir() {{
\t\t\tabs, _ := filepath.Abs(rel)
\t\t\treturn abs, nil
\t\t}}
\t}}
\tif p, err := exec.LookPath(wireParentSlug); err == nil {{
\t\treturn p, nil
\t}}
\treturn "", fmt.Errorf("parent binary %q not found — build ../%s-cli or set %s_BIN", wireParentSlug, wireParentSlug, strings.ToUpper(strings.ReplaceAll(wireParentSlug, "-", "_")))
}}

// Search delegates to the {parent} parent CLI with this slug's brand filter.
func (c *Client) Search(query string, page, pageSize int) (*HotelSearchResult, error) {{
\tbrand := c.Brand
\tif brand == "" {{
\t\tbrand = {json_str(default_brand_name)}
\t}}
\tbin, err := wireParentBin()
\tif err != nil {{
\t\treturn nil, err
\t}}
\targs := []string{{"search", "--json", "--brand", brand, "--limit", fmt.Sprint(pageSize), "--page", fmt.Sprint(page)}}
\targs = append(args, strings.Fields(query)...)
\tout, err := exec.Command(bin, args...).Output()
\tif err != nil {{
\t\tif ee, ok := err.(*exec.ExitError); ok && len(ee.Stderr) > 0 {{
\t\t\treturn nil, fmt.Errorf("%s: %s", wireParentSlug, strings.TrimSpace(string(ee.Stderr)))
\t\t}}
\t\treturn nil, fmt.Errorf("%s: %w", wireParentSlug, err)
\t}}
\tvar res HotelSearchResult
\tif err := json.Unmarshal(out, &res); err != nil {{
\t\treturn nil, fmt.Errorf("%s json: %w", wireParentSlug, err)
\t}}
\treturn &res, nil
}}
"""


